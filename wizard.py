def do_test(integration: bool = False) -> None:
    header("Test")

    # wordfreq silently disables the common-word FP filter when missing.
    # That makes the false-positive tests pass incorrectly — catch it before pytest runs.
    try:
        import importlib
        importlib.import_module("wordfreq")
    except ModuleNotFoundError:
        err("wordfreq is not installed — tests would produce WRONG results without it.")
        err("The false-positive filter is disabled silently, causing FP tests to pass incorrectly.")
        print()
        err("Run:  python3 wizard.py setup   (or: pip install -r requirements.txt)")
