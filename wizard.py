def wizard(initial_engagement: str = "") -> None:
    header(f"DontFeedTheAI — Setup Wizard  ({SYSTEM} {platform.machine()})")

    # ── 0. New or existing engagement ─────────────────────────────────────────
    if not initial_engagement:
        saved = _load_env()
        saved_id  = saved.get("engagement_id", "")
        saved_vps = saved.get("vps_host", "")
        has_saved = bool(saved_id and saved_vps)

        print(f"  {B}Select an engagement:{N}\n")
        print(f"  {B}[1]{N} New engagement  — configure everything from scratch.")
        if has_saved:
            print(f"  {B}[2]{N} {saved_id}  ({saved_vps})")
        print()

        default_choice = "2" if has_saved else "1"
        flow = ask("Choice", default=default_choice)
        print()

        if flow == "2" and has_saved:
            _wizard_existing(saved)
            return
        else:
            default_name = f"pentest-{datetime.now().strftime('%Y%m%d')}"
            initial_engagement = ask("Engagement / client name", default=default_name)

    # ── 1. Deployment mode ────────────────────────────────────────────────────
    print(f"\n  {B}Deployment mode:{N}\n")
    print(f"  {B}[1]{N} VPS         — recommended. Proxy + Ollama run remotely, SSH tunnel to localhost.")
    print(f"  {B}[2]{N} Local       — everything on this machine (Apple Silicon / Linux).")
    print(f"  {B}[3]{N} Docker      — fully containerized, local.\n")
    mode = ask("Choice", default="1")

    mode_name = {"1": "vps", "2": "local", "3": "docker"}.get(mode, "local")
    cfg: dict = {"engagement_id": initial_engagement, "deploy_mode": mode_name}

    # ── 2. VPS config ─────────────────────────────────────────────────────────
    if mode == "1":
        print()
        cfg["vps_host"]   = ask("VPS address (e.g. root@1.2.3.4)")
        cfg["proxy_port"] = ask("Proxy port", default="5555")

        print()
        if not IS_WIN:
            # Suggest the first standard SSH key that exists on this machine.
            ssh_dir = Path.home() / ".ssh"
            default_key = next(
                (str(ssh_dir / name) for name in ("id_ed25519", "id_rsa", "id_ecdsa")
                 if (ssh_dir / name).exists()),
                str(ssh_dir / "id_ed25519"),
            )
            if not ssh_available():
                warn("ssh not found in PATH — install OpenSSH or use WSL.")
            cfg["ssh_key"] = ask("SSH private key path", default=default_key)
        else:
            if ssh_available():
                ok("OpenSSH detected — using system ssh")
                cfg["ssh_key"] = ask("SSH private key path (leave blank to use password)", default="")
            if not cfg.get("ssh_key"):
                warn("No SSH key — will use password authentication (requires paramiko).")
                cfg["ssh_pass"] = ask("VPS root password", secret=True)

    # ── 3. Pentest working directory ──────────────────────────────────────────
    if mode == "1":
        default_dir = str(Path.home() / "pentests" / cfg["engagement_id"])
        print()
        info("Claude will launch from this directory (keeps your pentest files isolated).")
        cfg["pentest_dir"] = ask("Working directory", default=default_dir)

    # ── 4. Ollama model ───────────────────────────────────────────────────────
    print()
    ram_gb = detect_ram_gb()
    model  = suggest_model(ram_gb)
    if ram_gb:
        info(f"Detected RAM: {ram_gb:.1f} GB → suggested model: {model}")
    else:
        info(f"Could not detect RAM — defaulting to {model}")
    cfg["ollama_model"] = ask("Ollama model", default=model)

    # ── 5. Write .env.vps ─────────────────────────────────────────────────────
    print()
    write_env(cfg)

    # ── 6. Next steps ─────────────────────────────────────────────────────────
    if mode == "1":
        print()
        if ask_yn("Deploy to VPS now?", default=True):
            print()
            do_deploy(cfg)
            print()
            if ask_yn("Open tunnel and launch Claude now?", default=True):
                print()
                do_connect(cfg)
            else:
                info("When ready:  python3 wizard.py connect")
        else:
            print()
            info("When ready:")
            info("  python3 wizard.py deploy   # deploy to VPS")
            info("  python3 wizard.py connect  # open tunnel + launch Claude")

    elif mode == "2":
        print()
        if ask_yn("Start proxy and launch Claude now?", default=True):
            print()
            do_run_local(cfg)
        else:
            info("To start manually:\n")
            if IS_WIN:
                print(f"     set ENGAGEMENT_ID={cfg['engagement_id']}")
                print(f"     python -m uvicorn src.main:app --host 0.0.0.0 --port 8080")
                print(f"     set ANTHROPIC_BASE_URL=http://localhost:8080")
                print(f"     claude")
            else:
                print(f"     ENGAGEMENT_ID={cfg['engagement_id']} uvicorn src.main:app --host 0.0.0.0 --port 8080")
                print(f"     # in another terminal:")
