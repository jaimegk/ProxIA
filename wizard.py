# ── Install global command ─────────────────────────────────────────────────────

def do_install() -> None:
    header(f"Install — global '{INSTALL_CMD}' command")
    wizard_path = ROOT.resolve() / "wizard.py"

    if IS_WIN:
        install_dir = Path(sys.executable).parent
        wrapper     = install_dir / f"{INSTALL_CMD}.bat"
        wrapper.write_text(f'@echo off\npython "{wizard_path}" %*\n')
        ok(f"Installed: {wrapper}")
    else:
        install_path = Path(f"/usr/local/bin/{INSTALL_CMD}")
        content      = f'#!/bin/sh\nexec python3 "{wizard_path}" "$@"\n'
        try:
            install_path.write_text(content)
            install_path.chmod(0o755)
        except PermissionError:
            import tempfile
            with tempfile.NamedTemporaryFile(mode="w", suffix=".sh", delete=False) as f:
                f.write(content)
                tmp = f.name
            subprocess.run(["sudo", "install", "-m", "755", tmp, str(install_path)], check=True)
            Path(tmp).unlink(missing_ok=True)
        ok(f"Installed: {install_path}")

    print()
    info("You can now run from any directory:")
    print(f"\n     {INSTALL_CMD}                 # full setup")
    print(f"     {INSTALL_CMD} connect          # open tunnel + launch Claude")
    print(f"     {INSTALL_CMD} deploy           # deploy to VPS")
    print(f"     {INSTALL_CMD} sync             # sync code updates")
    print()


# ── Deploy ────────────────────────────────────────────────────────────────────

def do_deploy(cfg: dict) -> None:
    header("Deploy — VPS")
    _prime_ssh_agent(cfg)
    vps        = cfg["vps_host"]
    proxy_port = cfg.get("proxy_port", "5555")
    info(f"Target     : {vps}")
    info(f"Remote dir : {REMOTE_DIR}")
    print()

    if IS_WIN:
        client = _paramiko_connect(cfg)
        info("Syncing project files...")
        _paramiko_run(client, f"mkdir -p {REMOTE_DIR}")
        _sftp_upload_dir(client, ROOT, REMOTE_DIR)
        ok("Sync done")
        info("Uploading .env...")
        sftp = client.open_sftp()
        sftp.put(str(ENV_FILE), f"{REMOTE_DIR}/.env")
        sftp.close()
        ok(".env uploaded")
        info("Checking Docker...")
        try:
            _paramiko_run(client, "docker --version && docker compose version")
        except RuntimeError:
            info("Installing Docker...")
            _paramiko_run(client, _DOCKER_INSTALL)
        ok("Docker ready")
        info("Starting containers...")
        try:
            _paramiko_run(client, f"cd {REMOTE_DIR} && docker compose -f docker-compose.vps.yml down --remove-orphans")
        except Exception:
            pass
        _paramiko_run(client, f"cd {REMOTE_DIR} && docker compose -f docker-compose.vps.yml up --build -d")
        ok("Containers started")
        client.close()
    else:
        info("Syncing project files...")
        _run_ssh(cfg, f"mkdir -p {REMOTE_DIR}")
        _rsync_to_vps(cfg)
        ok("Sync done")
        info("Uploading .env...")
        # Ensure PROXY_SECRET is stripped before upload — it breaks the Anthropic SDK
        # (TypeScript URL resolution discards any base URL path prefix).
        if ENV_FILE.exists():
            cleaned = "\n".join(
                l for l in ENV_FILE.read_text().splitlines()
                if not l.startswith("PROXY_SECRET=")
            ) + "\n"
            ENV_FILE.write_text(cleaned)
        _scp_env(cfg)
        ok(".env uploaded")
        info("Checking Docker...")
        if _run_ssh(cfg, "docker --version 2>/dev/null && docker compose version 2>/dev/null", check=False).returncode != 0:
            info("Installing Docker...")
            _run_ssh_live(cfg, _DOCKER_INSTALL)
        ok("Docker ready")
        info(f"Blocking port {proxy_port} from external access (SSH tunnel only)...")
        _run_ssh(cfg, (
            f"which ufw >/dev/null 2>&1 || apt-get install -y -q ufw; "
            f"ufw allow 22/tcp >/dev/null 2>&1; "
            f"ufw deny {proxy_port}/tcp >/dev/null 2>&1; "
            f"ufw --force enable >/dev/null 2>&1"
        ), check=False)
        ok("Firewall configured")
        info("Starting containers...")
        # Bring down first to release port bindings cleanly, then rebuild.
        # Sleep gives the kernel time to release TCP ports before docker re-binds them.
        _run_ssh(cfg, f"cd {REMOTE_DIR} && docker compose -f docker-compose.vps.yml down --remove-orphans --timeout 15 2>&1; sleep 3", check=False)
        _run_ssh_live(cfg, f"cd {REMOTE_DIR} && docker compose -f docker-compose.vps.yml up --build -d")
        ok("Containers started")

    # Wait for proxy via a temporary tunnel
    info("Waiting for proxy to come up (first run pulls the Ollama model — may take a minute)...")
    if not IS_WIN:
        tunnel = _open_tunnel_unix(cfg)
        try:
            if _wait_for_proxy(int(proxy_port), timeout=90):
                ok("Proxy healthy")
            else:
                warn("Proxy didn't respond in 90s — it may still be pulling the model.")
                info(f"Check: ssh {vps} 'cd {REMOTE_DIR} && docker compose -f docker-compose.vps.yml logs -f proxy'")
        finally:
            tunnel.terminate()
            tunnel.wait()


# ── Sync ─────────────────────────────────────────────────────────────────────

def do_sync(cfg: dict) -> None:
    header("Sync — push code to VPS and rebuild proxy")
    _prime_ssh_agent(cfg)
    info("Syncing files...")
    if IS_WIN:
        client = _paramiko_connect(cfg)
        _sftp_upload_dir(client, ROOT, REMOTE_DIR)
        ok("Sync done")
        info("Rebuilding proxy container...")
        _paramiko_run(client, (
            f"cd {REMOTE_DIR} && "
            "docker compose -f docker-compose.vps.yml build proxy --quiet && "
            "docker compose -f docker-compose.vps.yml up -d --no-deps proxy"
        ))
        client.close()
    else:
        _rsync_to_vps(cfg)
        ok("Sync done")
        info("Rebuilding proxy container...")
        _run_ssh_live(cfg, (
            f"cd {REMOTE_DIR} && "
            "docker compose -f docker-compose.vps.yml build proxy --quiet 2>&1 | tail -3 && "
            "docker compose -f docker-compose.vps.yml up -d --no-deps proxy"
        ))
    ok("Proxy rebuilt and restarted")


# ── Connect ───────────────────────────────────────────────────────────────────

def do_connect(cfg: dict) -> None:
    proxy_port    = int(cfg.get("proxy_port", "5555"))
    engagement_id = cfg.get("engagement_id", "default")

    header("Connect — opening tunnel and launching Claude")
    _prime_ssh_agent(cfg)

    # Security: SSH tunnel + UFW port blocking protect the proxy.
    # Path-based secrets don't work because the Anthropic SDK constructs
    # URLs with absolute paths that replace any base URL path component.
    base_url  = f"http://localhost:{proxy_port}"

    # Free stale port bindings
    for port in [proxy_port, OLLAMA_PORT]:
        _kill_port(port)

    # Open tunnel
    info(f"Opening SSH tunnel to {cfg['vps_host']}...")
    info(f"  localhost:{proxy_port}  → VPS:{proxy_port}  (proxy)")
    info(f"  localhost:{OLLAMA_PORT} → VPS:{OLLAMA_PORT} (Ollama)")

    tunnel_proc   = None
    tunnel_client = None

    if IS_WIN:
        tunnel_client = _open_tunnel_win(cfg)
    else:
        tunnel_proc = _open_tunnel_unix(cfg)

    def _cleanup() -> None:
        if tunnel_proc and tunnel_proc.poll() is None:
            tunnel_proc.terminate()
            try:
                tunnel_proc.wait(timeout=3)
            except subprocess.TimeoutExpired:
                tunnel_proc.kill()
        if tunnel_client:
            try:
                tunnel_client.close()
            except Exception:
                pass
        print(f"\n  {Y}▸{N}  Tunnel closed.")

    atexit.register(_cleanup)

    # Wait for proxy
    info("Waiting for proxy...")
    if not _wait_for_proxy(proxy_port, timeout=30):
        err("Proxy didn't respond in 30s. Is the VPS stack running?")
        info("Run:  python3 wizard.py deploy")
        sys.exit(1)
    ok(f"Proxy ready")

    # Resolve working directory
    pentest_dir = cfg.get("pentest_dir", "")
    launch_dir  = pentest_dir or str(Path.home() / "pentests" / engagement_id)
    Path(launch_dir).mkdir(parents=True, exist_ok=True)

    # Open audit UI in browser
    audit_url = f"http://localhost:{proxy_port}/audit"
    try:
        webbrowser.open(audit_url)
    except Exception:
        pass

    # Session summary
    print()
    print(HR)
    print(f"{B}  Session active{N}")
    print(HR)
    print(f"  Engagement  : {engagement_id}")
    print(f"  Proxy       : {base_url}")
    print(f"  Audit UI    : {audit_url}")
    print(f"  Working dir : {C}{launch_dir}{N}")
    print(HR)
    print()

    # Launch Claude
    claude_bin = shutil.which("claude")
    if not claude_bin:
        err("claude not found in PATH. Install Claude Code first: https://claude.ai/code")
        sys.exit(1)

    env = dict(os.environ)
    env["ANTHROPIC_BASE_URL"] = base_url
    env["ENGAGEMENT_ID"]      = engagement_id
    env["OLLAMA_HOST"]        = f"http://localhost:{OLLAMA_PORT}"

    # Smoke test: verify Claude can reach the proxy before opening the full session
    info("Testing proxy → Claude connection...")
    try:
        test = subprocess.run(
            [claude_bin, "-p", "reply with exactly: proxy ok"],
            env=env, cwd=launch_dir,
            capture_output=True, text=True, timeout=30,
        )
        output = (test.stdout + test.stderr).strip()
        if test.returncode == 0 and "403" not in output and "Forbidden" not in output:
            ok("Proxy connection verified.")
        else:
            err("Proxy test failed. Claude returned:")
            print(f"\n    {output[:300]}\n")
            if "403" in output or "missing proxy token" in output:
                warn("The proxy is rejecting requests (403).")
                warn("The running container may still have PROXY_SECRET set from a previous deploy.")
                warn("Fix: python3 wizard.py → Deploy (will restart containers cleanly).")
            sys.exit(1)
    except subprocess.TimeoutExpired:
        warn("Smoke test timed out — launching Claude anyway.")
    except Exception as e:
        warn(f"Smoke test skipped: {e}")

    # Let Claude handle Ctrl+C itself; restore after it exits
    if not IS_WIN:
        old_sigint = signal.signal(signal.SIGINT, signal.SIG_IGN)
    try:
        subprocess.run([claude_bin], env=env, cwd=launch_dir)
    finally:
        if not IS_WIN:
            signal.signal(signal.SIGINT, old_sigint)


# ── Tunnel only ───────────────────────────────────────────────────────────────

def do_tunnel(cfg: dict, open_audit: bool = False) -> None:
    proxy_port = int(cfg.get("proxy_port", "5555"))

    header("Tunnel — SSH only")
    _prime_ssh_agent(cfg)

    for port in [proxy_port, OLLAMA_PORT]:
        _kill_port(port)

    info(f"Opening SSH tunnel to {cfg['vps_host']}...")
    tunnel_proc = _open_tunnel_unix(cfg)

    def _cleanup() -> None:
        if tunnel_proc.poll() is None:
            tunnel_proc.terminate()
            try:
                tunnel_proc.wait(timeout=3)
            except subprocess.TimeoutExpired:
                tunnel_proc.kill()
        info("Tunnel closed.")

    atexit.register(_cleanup)

    if not _wait_for_proxy(proxy_port, timeout=30):
        err("Proxy didn't respond. Is the VPS stack running?")
        sys.exit(1)

    audit_url = f"http://localhost:{proxy_port}/audit"
    ok(f"Tunnel ready — localhost:{proxy_port}")
    info(f"Audit UI : {audit_url}")
    info("Press Ctrl+C to close tunnel.")

    if open_audit:
        try:
            webbrowser.open(audit_url)
        except Exception:
            pass

    try:
        tunnel_proc.wait()
    except KeyboardInterrupt:
        pass


