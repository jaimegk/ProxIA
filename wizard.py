# ── Local proxy ──────────────────────────────────────────────────────────────

def do_run_local(cfg: dict) -> None:
    port          = cfg.get("port", "8080")
    engagement_id = cfg.get("engagement_id", "default")

    venv_uvicorn = ROOT / ".venv" / "bin" / "uvicorn"
    uvicorn_bin  = str(venv_uvicorn) if venv_uvicorn.exists() else "uvicorn"

    env = dict(os.environ)
    env["ENGAGEMENT_ID"] = engagement_id
    env["PYTHONPATH"]    = str(ROOT)

    info(f"Starting proxy on localhost:{port}...")
    proc = subprocess.Popen(
        [uvicorn_bin, "src.main:app", "--host", "0.0.0.0", "--port", str(port)],
        env=env, cwd=str(ROOT),
        stdout=subprocess.DEVNULL, stderr=subprocess.PIPE,
    )

    def _stop():
        if proc.poll() is None:
            proc.terminate()
            try:
                proc.wait(timeout=3)
            except subprocess.TimeoutExpired:
                proc.kill()
        info("Proxy stopped.")

    atexit.register(_stop)

    if not _wait_for_proxy(int(port), timeout=15):
        out = proc.stderr.read().decode() if proc.stderr else ""
        err("Proxy failed to start.")
        if out:
            print(out[:500])
        sys.exit(1)

    ok(f"Proxy running on localhost:{port}")

    base_url  = f"http://localhost:{port}"
    claude_bin = shutil.which("claude")
    if not claude_bin:
        err("claude not found in PATH. Install Claude Code: https://claude.ai/code")
        info(f"Set ANTHROPIC_BASE_URL={base_url} and run claude manually.")
        sys.exit(1)

    claude_env = dict(os.environ)
    claude_env["ANTHROPIC_BASE_URL"] = base_url
    claude_env["ENGAGEMENT_ID"]      = engagement_id

    print()
    print(HR)
    print(f"{B}  Session active (local){N}")
    print(HR)
