# ── SSH helpers (Windows / paramiko) ─────────────────────────────────────────

def _paramiko_connect(cfg: dict):
    try:
        import paramiko
    except ImportError:
        err("paramiko is required on Windows.  Run:  pip install paramiko")
        sys.exit(1)
    host = cfg["vps_host"].split("@")[-1]
    user = cfg["vps_host"].split("@")[0] if "@" in cfg["vps_host"] else "root"
    key  = cfg.get("ssh_key", "")
    pwd  = cfg.get("ssh_pass", "")
    client = paramiko.SSHClient()
    client.set_missing_host_key_policy(paramiko.AutoAddPolicy())
    if key and Path(key).exists():
        client.connect(host, username=user, key_filename=key)
    elif pwd:
        client.connect(host, username=user, password=pwd)
    else:
        err("No SSH key or password provided.")
        sys.exit(1)
    return client


def _paramiko_run(client, cmd: str) -> str:
    _, stdout, stderr = client.exec_command(cmd)
    out = stdout.read().decode()
    rc  = stdout.channel.recv_exit_status()
    if rc != 0:
        raise RuntimeError(f"Remote command failed:\n{stderr.read().decode()}")
    return out


def _sftp_upload_dir(client, local_root: Path, remote_dir: str) -> None:
    sftp = client.open_sftp()

    def _upload(local: Path, remote: str) -> None:
        for item in local.iterdir():
            if item.name in _SFTP_EXCLUDE or item.suffix in (".pyc", ".db"):
                continue
            rem_path = f"{remote}/{item.name}"
            if item.is_dir():
                try:
                    sftp.mkdir(rem_path)
                except OSError:
                    pass
                _upload(item, rem_path)
            else:
                sftp.put(str(item), rem_path)

    _upload(local_root, remote_dir)
    sftp.close()


# ── Port management ───────────────────────────────────────────────────────────

def _kill_port(port: int) -> None:
    if IS_WIN:
        try:
            out = subprocess.check_output(
                f"netstat -ano | findstr :{port}", shell=True, text=True
            )
            for line in out.splitlines():
                parts = line.split()
                if parts and parts[-1].isdigit():
                    subprocess.run(["taskkill", "/F", "/PID", parts[-1]],
                                   capture_output=True, check=False)
        except Exception:
            pass
    else:
        try:
            result = subprocess.run(
                ["lsof", "-ti", f"tcp:{port}"], capture_output=True, text=True
            )
            for pid in result.stdout.strip().splitlines():
                subprocess.run(["kill", pid.strip()], check=False)
            if result.stdout.strip():
                time.sleep(0.5)
        except Exception:
            pass


# ── Health check ──────────────────────────────────────────────────────────────

def _wait_for_proxy(port: int, timeout: int = 30) -> bool:
    url = f"http://localhost:{port}/health"
    for _ in range(timeout):
        try:
            with urllib.request.urlopen(url, timeout=2) as r:
                if r.status == 200:
                    return True
        except Exception:
            pass
        time.sleep(1)
    return False


# ── Tunnel ────────────────────────────────────────────────────────────────────

def _open_tunnel_unix(cfg: dict) -> subprocess.Popen:
    proxy_port = cfg.get("proxy_port", "5555")
    cmd = ["ssh"] + _ssh_opts(cfg) + [
        "-N", "-o", "ExitOnForwardFailure=yes",
        "-L", f"{proxy_port}:localhost:{proxy_port}",
        "-L", f"{OLLAMA_PORT}:localhost:{OLLAMA_PORT}",
        cfg["vps_host"],
    ]
    return subprocess.Popen(cmd, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)


def _open_tunnel_win(cfg: dict):
    import threading, socket, select
    client = _paramiko_connect(cfg)
    proxy_port = int(cfg.get("proxy_port", "5555"))
    transport = client.get_transport()

    def _pipe(sock, chan):
        while True:
            r, _, _ = select.select([sock, chan], [], [])
            if sock in r:
                d = sock.recv(4096)
                if not d: break
                chan.send(d)
            if chan in r:
                d = chan.recv(4096)
                if not d: break
                sock.send(d)
        sock.close()
        chan.close()

    def _forward(local_port: int, remote_port: int) -> None:
        srv = socket.socket()
        srv.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
        srv.bind(("localhost", local_port))
        srv.listen(5)
        while True:
            try:
                conn, addr = srv.accept()
                chan = transport.open_channel(
                    "direct-tcpip", ("localhost", remote_port), addr
                )
                threading.Thread(target=_pipe, args=(conn, chan), daemon=True).start()
            except Exception:
                break

    for lp, rp in [(proxy_port, proxy_port), (OLLAMA_PORT, OLLAMA_PORT)]:
        threading.Thread(target=_forward, args=(lp, rp), daemon=True).start()

    return client  # caller holds reference; close() to shut down


