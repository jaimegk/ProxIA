def do_docker(action: str) -> None:
    compose_file = ROOT / "docker-compose.yml"
    vps_file     = ROOT / "docker-compose.vps.yml"
    f = str(vps_file) if vps_file.exists() and not compose_file.exists() else str(compose_file)

    if action == "up":
        header("Docker — start containers")
        subprocess.run(["docker", "compose", "-f", f, "up", "-d"], cwd=str(ROOT), check=True)
        model = os.environ.get("OLLAMA_MODEL", "qwen3:1.7b")
        info(f"Pulling Ollama model {model} (first run may take a minute)...")
        subprocess.run(
            ["docker", "compose", "-f", f, "exec", "ollama", "ollama", "pull", model],
            cwd=str(ROOT), check=False,
        )
        ok("Stack running")
    elif action == "down":
        header("Docker — stop containers")
        subprocess.run(["docker", "compose", "-f", f, "down"], cwd=str(ROOT), check=True)
        ok("Stack stopped")
    else:
        err(f"Unknown docker action: {action}. Use: up | down")
        sys.exit(1)


def do_vault(action: str) -> None:
    if action == "stats":
        try:
            with urllib.request.urlopen("http://localhost:8080/health", timeout=3) as r:
                import json
                print(json.dumps(json.loads(r.read()), indent=2))
        except Exception:
            err("Proxy not reachable on localhost:8080. Is it running?")
    elif action == "clear":
        engagement = os.environ.get("ENGAGEMENT_ID", "")
        if not engagement:
            engagement = ask("Engagement ID to clear")
        if not ask_yn(f"Clear vault for '{engagement}'?", default=False):
            info("Cancelled.")
            return
        db = ROOT / "data" / "pii_vault.db"
        if not db.exists():
            err(f"Database not found: {db}")
            sys.exit(1)
        subprocess.run(
            ["sqlite3", str(db), f"DELETE FROM pii_vault WHERE engagement='{engagement}';"],
            check=True,
        )
        ok(f"Vault cleared for engagement '{engagement}'")
    else:
        err(f"Unknown vault action: {action}. Use: stats | clear")
        sys.exit(1)


