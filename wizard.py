def err(msg: str)  -> None: print(f"  {R}✗{N}  {msg}")
def info(msg: str) -> None: print(f"  {C}▸{N}  {msg}")


def detect_ram_gb() -> float | None:
    try:
        import psutil
        return psutil.virtual_memory().total / (1024 ** 3)
    except ImportError:
        pass
    try:
        if IS_MAC:
            out = subprocess.check_output(["sysctl", "-n", "hw.memsize"], text=True)
            return int(out.strip()) / (1024 ** 3)
        if IS_LINUX:
            mem = Path("/proc/meminfo").read_text()
            kb  = int(re.search(r"MemTotal:\s+(\d+)", mem).group(1))
            return kb / (1024 ** 2)
        if IS_WIN:
            out = subprocess.check_output(
                ["wmic", "computersystem", "get", "TotalPhysicalMemory"],
                text=True, shell=True,
            )
            return int(out.strip().split()[-1]) / (1024 ** 3)
    except Exception:
        pass
    return None


def suggest_model(ram_gb: float | None) -> str:
    if ram_gb is None: return "qwen3:1.7b"
    if ram_gb >= 16:   return "qwen3:8b"
    if ram_gb >= 8:    return "qwen3:4b"
    if ram_gb >= 4:    return "qwen3:1.7b"
    return "qwen3:0.6b"


